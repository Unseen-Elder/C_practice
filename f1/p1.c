#include<stdio.h>
int main()
{
    int a = 2;
    float b = 8.5;
    char c = 'a';
    // char is always enclosed in quations.
    printf("the value of a is %d \n",a);
    printf("the value of a is %f \n",b);
    printf("the value of a is %c \n",c);
    printf("the value of a+b is %f \n",a+b);
}