#include<stdio.h>
int main()
{
    int i=5, j=5;

    printf("The value after i++ is %d\n",i++); //phle print fir increment in i++
    printf("the value of i is %d\n",i);

    printf("The value after ++j is %d\n",++j); // phle increment fir print in ++j
    printf("the value of j is %d\n",j);
    return 0;
}